// Fungsi untuk mendapatkan novel berdasarkan ID
function getNovelById($id) {
    global $allNovels;
    
    foreach ($allNovels as $novel) {
        if ($novel['id'] == $id) {
            return $novel;
        }
    }
    
    return null;
}

// Fungsi untuk mendapatkan novel berdasarkan kategori
function getNovelsByCategory($categoryId, $limit = null) {
    global $allNovels;
    
    $result = array_filter($allNovels, function($novel) use ($categoryId) {
        return $novel['category'] === $categoryId;
    });
    
    if ($limit !== null) {
        return array_slice($result, 0, $limit);
    }
    
    return $result;
}

// Fungsi untuk mendapatkan novel berdasarkan kata kunci
function searchNovels($keyword) {
    global $allNovels;
    
    return array_filter($allNovels, function($novel) use ($keyword) {
        return (
            stripos($novel['title'], $keyword) !== false || 
            stripos($novel['author'], $keyword) !== false || 
            stripos($novel['description'], $keyword) !== false
        );
    });
}
